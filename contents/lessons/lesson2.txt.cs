# Lesson 2: Variables and Data Types in C#

In this lesson, you'll learn about variables and the most common data types in C#.

A **variable** is a named container used to store data. In C#, you must declare a variable's type before assigning a value to it.

Common data types:
- `int`: Integer numbers
- `string`: Text
- `bool`: True or false
- `double`: Decimal numbers

```cs
int age = 30;
string name = "Alice";
bool isStudent = true;
double height = 1.75;

$"{name} is {age} years old, {height}m tall. Student: {isStudent}"
